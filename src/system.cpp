/* System class constructor */
System::System() {
    vector<int> process_Ids = LinuxParser::Pids();
    for(unsigned char index = 0; index < process_Ids.size(); ++index)
    {
        processes_.push_back(*(new Process(process_Ids[index])));
        delete Process;
    }
}